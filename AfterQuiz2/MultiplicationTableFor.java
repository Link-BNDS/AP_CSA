import java.util.Scanner;
public class MultiplicationTableFor {
    public static void main (String args[]){
        int p, q = 5;    
    for (p=0; p<5; p++);    //notice the semicolon
        q = q+1;
    System.out.print (p +" "+ q);

    }
    // for (intialization; condition; increment/decrement)
}